#include <iostream>
#include <math.h>

using namespace std;

int main()
{
	int a, b;
	cout << " a = ";
	cin >> a;
	cout << " b = ";
	cin >> b;
	cout << " a + b = " << a + b << endl;
	cout << " a * b = " << a * b << endl;
	cout << " a ^ 2 = " << pow(a, 2) << endl;
	cout << " b ^ 2 = " << pow(b, 2) << endl;
	return 0;
}